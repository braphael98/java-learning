public class Mob {
        String nome;
        int hp;
        int atk;
        int def;

        int xpDrop;
        int level;
        //TODO: loottable[]


        public String getNome() {
            return nome;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        public int getHp() {
            return hp;
        }

        public void setHp(int hp) {
            this.hp = hp;
        }

        public int getAtk() {
            return atk;
        }

        public void setAtk(int atk) {
            this.atk = atk;
        }

        public int getDef() {
            return def;
        }

        public void setDef(int def) {
            this.def = def;
        }

        public int getXpDrop() {
            return xpDrop;
        }

        public void setXpDrop(int xpDrop) {
            this.xpDrop = xpDrop;
        }

        public int getLevel() {
            return level;
        }

        public void setLevel(int level) {
            this.level = level;
        }
    }


